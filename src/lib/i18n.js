function resolveKey(locale, key) {
  return key.split('.').reduce((value, segment) => value?.[segment], translations[locale]);
}

function interpolate(template, vars = {}) {
  if (typeof template !== 'string') return template;
  return template.replace(/\{\{(.*?)\}\}/g, (_, key) => String(vars[key.trim()] ?? ''));
}

export function getStoredLocale() {
  if (typeof window === 'undefined') return DEFAULT_LOCALE;
  const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  return saved && LOCALES[saved] ? saved : DEFAULT_LOCALE;
}

export function setStoredLocale(locale) {
  if (typeof window === 'undefined') return;
  window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
}

export function getLocaleTag(locale = DEFAULT_LOCALE) {
  return locale === 'uz' ? 'uz-UZ' : 'en-US';
}

export function translate(locale = DEFAULT_LOCALE, key, vars = {}) {
  const value = resolveKey(locale, key) ?? resolveKey(DEFAULT_LOCALE, key) ?? key;
  if (Array.isArray(value)) return value;
  return interpolate(value, vars);
}
